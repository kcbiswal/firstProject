package Oops;

 class Account {
     private String name;
     private int password;

     //getter and setter method
     public String getName(){
         return  name;
     }
     public void setName(String name){
         this.name=name;
     }
     public int getPassword(){
         return password;
     }
     public void setPassword(int password){
         this.password=password;
     }
}
